    public String aprox(String precio) {
        int pos = -1;
        for (int i = 0; i < precio.length(); i++) {
            if (precio.charAt(i) == '.') {
                pos = i;
                break;
            }
        }
        String o = "";
        if (pos != -1) {
            int posi = pos + 3;
            for (int i = 0; (i < precio.length()) && (i < posi); i++) {
                o += precio.charAt(i) + "";
            }
        }
        if (o.isEmpty()) {
            return precio;
        } else {
            return o;
        }
    }

